public class Solution
 {
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        int i,m,n;
        n=sc.nextInt();
        for(i=1;i<=10;i++)
        {
            m=n*i;
          System.out.println(n + " x "+i+" = "+m);
        }
        
        
    }
    
}
